package com.foodmap.common.domain;

/**
 * 推荐内容可见范围，是推荐、评论和社区统计权限判断的核心枚举。
 *
 * <p>评论和评论图片继承所属推荐的可见范围；只有 {@link #PUBLIC} 推荐进入全站社区统计。</p>
 */
public enum VisibilityType {
    /**
     * 仅作者可见，不进入任何公开统计。
     */
    PRIVATE,
    /**
     * 指定用户可见，需要结合可见规则表中的目标用户判断权限。
     */
    SPECIFIC_USERS,
    /**
     * 好友可见，需要调用关系服务确认当前用户与作者存在有效好友关系。
     */
    FRIENDS,
    /**
     * 情侣可见，需要调用关系服务确认当前用户与作者存在有效情侣关系。
     */
    COUPLE,
    /**
     * 群组可见，需要结合可见规则表和关系服务群组成员校验。
     */
    GROUP,
    /**
     * 全部公开，所有登录用户可见，并允许进入全站公开社区统计。
     */
    PUBLIC
}
